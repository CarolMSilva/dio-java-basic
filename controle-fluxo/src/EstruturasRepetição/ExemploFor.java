package EstruturasRepetição;
public class ExemploFor {
    public static void main(String[] args) {
        for(int carneirinhos = 1 ; carneirinhos <=20; carneirinhos ++) {
//bloco de declaração da minha variável inteira em seguida vou para o bloco de validação e o terceiro bloco diz como quero incrementar o seu bloco 1 ou seja o carneirinho
            System.out.println("Contando carneirinhos" +carneirinhos);
//agora a instrução do bloco ou seja o que quero que a variável o código faça
        }
        System.out.println("Joãozinho dormiu");
//se quizermos confirmar que o nosso código continua executando após a impresão podemos colocar
    }
}
